package application;

public class App {
	public static void main(String[] args) {
		boolean bValue = true;
		char c = '4';
		
		//integer numeric types
		/* byte, short, integer, long - each one sets aside a certain
		 * number of bytes in memory
		 * byte = 8 bits
		 * -128 to 127
		 * short = 62k approx
		 * integer = normal numbers
		 * long = for very long values; have to append an 'L'
		 * to the end, cuz Java initially thinks it's an int
		 * 
		 */
		
		byte byteValue = 123;
		short shortValue = 30000;
		int intValue = 423434;
		long longValue = 34235436784848L;
		
	}
}
